    private void btnBuscarTutoradoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarTutoradoActionPerformed
        // TODO add your handling code here:
        String nombre;
        String carrera;
        String estado;
        String enunciadoSQL = " ";
        
        nombre = txtNombre.getText().toUpperCase().trim();
        carrera = txtCarrera.getText().toUpperCase().trim();
        estado = txtEstado.getText().toUpperCase().trim();
        
        if(nombre.length()>0){
            enunciadoSQL="SELECT * FROM tutorados WHERE nombrecompleto LIKE '%"+nombre+"%'";
        }else if(carrera.length()>0){
            enunciadoSQL="SELECT * FROM tutorados WHERE carrera LIKE '%"+carrera+"%'";
        }else if(estado.length()>0){
            enunciadoSQL="SELECT * FROM tutorados WHERE estadocivil LIKE '%"+estado+"%'";
        }
        
        con.consultar(enunciadoSQL);
        int num = con.getNumRegistros();
        if(con.irPrimero()){
        for(int i=0;i<num;i++){
            tblDatos.setValueAt(con.obtenerCampo("clavetutorado"), i, 0);
            tblDatos.setValueAt(con.obtenerCampo("nombrecompleto"), i, 1);
            tblDatos.setValueAt(con.obtenerCampo("direccion"), i, 2);
            tblDatos.setValueAt(con.obtenerCampo("carrera"), i, 3);
            tblDatos.setValueAt(con.obtenerCampo("genero"), i, 4);
            tblDatos.setValueAt(con.obtenerCampo("edad"), i, 5);
            tblDatos.setValueAt(con.obtenerCampo("estadocivil"), i, 6);
            con.irSiguiente();
           }
        }
    }//GEN-LAST:event_btnBuscarTutoradoActionPerformed

